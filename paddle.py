from turtle import Turtle
PADDLES = {1: (350, 0),
           2: (-350, 0)}


class Paddle(Turtle):

    def __init__(self, paddle_num):
        super().__init__()
        self.shape("square")
        self.color("white")
        self.shapesize(stretch_wid=5, stretch_len=1)
        self.penup()
        self.goto(PADDLES[paddle_num])

    def move_up(self):
        new_y = self.ycor() + 20
        self.goto(self.xcor(), new_y)

    def move_down(self):
        new_y = self.ycor() - 20
        self.goto(self.xcor(), new_y)
